import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const manifestPath = path.join(repoRoot, "editorial/evidence-searches.json");
const outputDir = path.join(repoRoot, "outputs/evidence-candidates");
const maxExcerptWords = 24;
const contextRadius = 2;

function resolvePath(value) {
  if (!value) return "";
  return path.isAbsolute(value) ? value : path.join(repoRoot, value);
}

function slug(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function normalizeLine(value) {
  return value.replace(/\s+/g, " ").trim();
}

function truncateWords(value, maxWords = maxExcerptWords) {
  const words = normalizeLine(value).split(/\s+/).filter(Boolean);
  return words.slice(0, maxWords).join(" ");
}

function ensureTextPath(record) {
  const textPath = resolvePath(record.localTextPath);
  if (textPath && existsSync(textPath)) return textPath;

  const pdfPath = resolvePath(record.localPdfPath);
  if (!pdfPath || !existsSync(pdfPath)) return "";

  const generatedDir = path.join(repoRoot, "tmp/pdfs/evidence-source-text");
  mkdirSync(generatedDir, { recursive: true });
  const generatedTextPath = path.join(generatedDir, `${record.sourceId}.txt`);
  execFileSync("pdftotext", ["-layout", pdfPath, generatedTextPath], { stdio: "inherit" });
  return generatedTextPath;
}

function findMatches(lines, terms) {
  const matches = [];
  const seen = new Set();
  terms.forEach((term) => {
    const lowerTerm = term.toLowerCase();
    lines.forEach((line, index) => {
      const normalized = normalizeLine(line);
      if (!normalized.toLowerCase().includes(lowerTerm)) return;
      const key = `${index}:${lowerTerm}`;
      if (seen.has(key)) return;
      seen.add(key);

      const contextStart = Math.max(0, index - contextRadius);
      const contextEnd = Math.min(lines.length - 1, index + contextRadius);
      const context = lines.slice(contextStart, contextEnd + 1).map(normalizeLine).filter(Boolean);
      matches.push({
        term,
        lineNumber: index + 1,
        shortExcerpt: truncateWords(normalized),
        contextSummarySource: context.join(" "),
      });
    });
  });
  return matches;
}

const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
mkdirSync(outputDir, { recursive: true });

const report = [];

manifest.forEach((record) => {
  const textPath = ensureTextPath(record);
  if (!textPath) {
    report.push({
      sourceId: record.sourceId,
      title: record.title,
      status: "missing-local-text",
      message: "No localTextPath found and no localPdfPath available.",
    });
    return;
  }

  const lines = readFileSync(textPath, "utf8").split(/\r?\n/);
  const sourceCandidates = {
    sourceId: record.sourceId,
    title: record.title,
    localTextPath: path.relative(repoRoot, textPath),
    generatedAt: new Date().toISOString(),
    candidateSectionIds: record.candidateSectionIds,
    candidates: [],
  };

  record.searches.forEach((search) => {
    const matches = findMatches(lines, search.terms);
    matches.slice(0, 25).forEach((match, index) => {
      sourceCandidates.candidates.push({
        id: `candidate-${record.sourceId}-${search.id}-${index + 1}`,
        sourceId: record.sourceId,
        searchId: search.id,
        matchedTerm: match.term,
        sourceLocator: `${record.title}; local extracted text line ${match.lineNumber}`,
        evidenceType: "short-excerpt-candidate",
        shortExcerpt: match.shortExcerpt,
        passageSummaryDraft: "",
        contextForLocalReview: match.contextSummarySource,
        candidateSectionIds: record.candidateSectionIds,
        tags: search.tags,
        reviewStatus: "candidate",
      });
    });
  });

  const outputPath = path.join(outputDir, `${slug(record.sourceId)}.json`);
  writeFileSync(outputPath, `${JSON.stringify(sourceCandidates, null, 2)}\n`);
  report.push({
    sourceId: record.sourceId,
    title: record.title,
    status: "ok",
    localTextPath: sourceCandidates.localTextPath,
    candidates: sourceCandidates.candidates.length,
    outputPath: path.relative(repoRoot, outputPath),
  });
});

writeFileSync(path.join(outputDir, "README.md"), renderReadme(report));
console.log(`Evidence candidate extraction complete: ${path.relative(repoRoot, outputDir)}`);
report.forEach((item) => {
  console.log(`- ${item.sourceId}: ${item.status}${item.candidates === undefined ? "" : `, ${item.candidates} candidates`}`);
});

function renderReadme(items) {
  const rows = items
    .map(
      (item) =>
        `| ${item.sourceId} | ${item.status} | ${item.candidates ?? 0} | ${item.localTextPath ?? ""} | ${item.outputPath ?? ""} |`,
    )
    .join("\n");
  return `# Evidence Candidate Extraction

Generated by \`npm run evidence:extract\`.

These files are local review aids. Candidate snippets are not automatically approved evidence items and should not be copied into public guidance without review.

| Source ID | Status | Candidates | Local text | Output |
| --- | --- | ---: | --- | --- |
${rows}
`;
}
